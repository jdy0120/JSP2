package vo;

public class ProductInfo {
	private String tp_name, tp_img;
	private int tp_id, tp_price;
	
	public String getTp_name() {
		return tp_name;
	}
	public void setTp_name(String tp_name) {
		this.tp_name = tp_name;
	}
	public String getTp_img() {
		return tp_img;
	}
	public void setTp_img(String tp_img) {
		this.tp_img = tp_img;
	}
	public int getTp_id() {
		return tp_id;
	}
	public void setTp_id(int tp_id) {
		this.tp_id = tp_id;
	}
	public int getTp_price() {
		return tp_price;
	}
	public void setTp_price(int tp_price) {
		this.tp_price = tp_price;
	}
	
}
